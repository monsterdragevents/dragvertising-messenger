interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  trigger?: React.ReactNode;
  className?: string;
}

export function EmojiPicker({ onEmojiSelect, trigger, className }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = React.useState<string>('Frequently Used');
  const [frequentlyUsed, setFrequentlyUsed] = React.useState<string[]>(() => {
    const stored = localStorage.getItem('emoji-frequently-used');
    return stored ? JSON.parse(stored) : EMOJI_CATEGORIES['Frequently Used'];
  });

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    
    // Update frequently used
    const updated = [emoji, ...frequentlyUsed.filter(e => e !== emoji)].slice(0, 10);
    setFrequentlyUsed(updated);
    localStorage.setItem('emoji-frequently-used', JSON.stringify(updated));
    
    // Update the category
    if (EMOJI_CATEGORIES['Frequently Used']) {
      EMOJI_CATEGORIES['Frequently Used'] = updated;
    }
  };

  const defaultTrigger = (
    <Button
      type="button"
      variant="ghost"
      size="icon"
      className={cn("h-9 w-9", className)}
      title="Add emoji"
    >
      <Smile className="h-4 w-4" />
    </Button>
  );

  return (
    <Popover>
      <PopoverTrigger asChild>
        {trigger || defaultTrigger}
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0" align="start">
        <div className="flex flex-col h-[400px]">
          {/* Category Tabs */}
          <div className="flex border-b overflow-x-auto">
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <button
                key={category}
                onClick={() => setSelectedCategory(category)}
                className={cn(
                  "px-3 py-2 text-xs font-medium whitespace-nowrap border-b-2 transition-colors",
                  selectedCategory === category
                    ? "border-primary text-primary"
                    : "border-transparent text-muted-foreground hover:text-foreground"
                )}
              >
                {category}
              </button>
            ))}
          </div>

          {/* Emoji Grid */}
          <ScrollArea className="flex-1 p-4">
            <div className="grid grid-cols-8 gap-2">
              {(EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES] || []).map((emoji, index) => (
                <button
                  key={`${emoji}-${index}`}
                  onClick={() => handleEmojiClick(emoji)}
                  className="text-2xl hover:bg-accent rounded-lg p-2 transition-colors aspect-square flex items-center justify-center"
                  title={emoji}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </ScrollArea>
        </div>
      </PopoverContent>
    </Popover>
  );
}

